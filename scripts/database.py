def replace_field_work_records(
    conn: sqlite3.Connection,
    work_date: str,
    zone_name: str,
    net_house_code: str,
    tasks: list[dict[str, Any]],
    account_name: str = "",
) -> bool:
    before_records = get_field_work_records_for_selection(conn, work_date, zone_name, net_house_code)
    conn.execute(
        """
        DELETE FROM field_work_record_crops
        WHERE work_date = ? AND zone_name = ? AND net_house_code = ?
        """,
        (work_date, zone_name, net_house_code),
    )
    conn.execute(
        """
        DELETE FROM field_work_records
        WHERE work_date = ? AND zone_name = ? AND net_house_code = ?
        """,
        (work_date, zone_name, net_house_code),
    )
    timestamp = taipei_now_text()
    rows = []
    crop_rows = []
    for task in tasks:
        task_key = str(task.get("taskKey") or "")
        if not task_key:
            continue
        crop_items = []
        raw_crop_items = task.get("cropItems")
        if isinstance(raw_crop_items, list):
            for raw_item in raw_crop_items:
                if not isinstance(raw_item, dict):
                    continue
                crop_key = str(raw_item.get("cropKey") or "")
                crop_name = str(raw_item.get("cropName") or "")
                tray_count = raw_item.get("trayCount")
                if crop_key or crop_name:
                    crop_items.append({"cropKey": crop_key, "cropName": crop_name, "trayCount": tray_count})
        if not crop_items and (task.get("cropKey") or task.get("cropName")):
            crop_items.append(
                {
                    "cropKey": str(task.get("cropKey") or ""),
                    "cropName": str(task.get("cropName") or ""),
                    "trayCount": None,
                }
            )
        first_crop = crop_items[0] if crop_items else {"cropKey": "", "cropName": ""}
        fertilizer_bag_count = task.get("fertilizerBagCount")
        rows.append(
            (
                work_date,
                zone_name,
                net_house_code,
                task_key,
                str(task.get("taskLabel") or ""),
                float(fertilizer_bag_count) if fertilizer_bag_count not in (None, "") else None,
                str(first_crop.get("cropKey") or ""),
                str(first_crop.get("cropName") or ""),
                str(account_name or "")[:80],
                timestamp,
            )
        )
        for index, crop_item in enumerate(crop_items, start=1):
            crop_rows.append(
                (
                    work_date,
                    zone_name,
                    net_house_code,
                    task_key,
                    index,
                    str(crop_item.get("cropKey") or ""),
                    str(crop_item.get("cropName") or ""),
                    float(crop_item.get("trayCount")) if crop_item.get("trayCount") not in (None, "") else None,
                    timestamp,
                )
            )
    conn.executemany(
        """
        INSERT INTO field_work_records (
            work_date,
            zone_name,
            net_house_code,
            task_key,
            task_label,
            fertilizer_bag_count,
            crop_key,
            crop_name,
            account_name,
            updated_at
        )
        VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
        """,
        rows,
    )
    conn.executemany(
        """
        INSERT INTO field_work_record_crops (
            work_date,
            zone_name,
            net_house_code,
            task_key,
            crop_index,
            crop_key,
            crop_name,
            tray_count,
            updated_at
        )
        VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)
        """,
        crop_rows,
    )
    after_records = get_field_work_records_for_selection(conn, work_date, zone_name, net_house_code)
    audit_logged = add_field_work_record_audit_log(
        conn,
        work_date,
        zone_name,
        net_house_code,
        "replace",
        account_name,
        before_records,
        after_records,
    )
    conn.commit()
    return audit_logged


def ensure_field_work_task_for_zones(
    conn: sqlite3.Connection,
    work_date: str,
    zone_names: list[str],
    task_key: str,
    task_label: str,
    account_name: str = "",
) -> int:
    normalized_zone_names = []
    seen = set()
    for zone_name in zone_names:
        zone_name = str(zone_name or "").strip()
        if zone_name and zone_name not in seen:
            seen.add(zone_name)
            normalized_zone_names.append(zone_name)
    if not normalized_zone_names:
        return 0

    timestamp = taipei_now_text()
    before_changes = conn.total_changes
    for zone_name in normalized_zone_names:
        conn.execute(
            """
            INSERT OR IGNORE INTO field_work_records (
                work_date,
                zone_name,
                net_house_code,
                task_key,
                task_label,
                fertilizer_bag_count,
                crop_key,
                crop_name,
                account_name,
                updated_at
            )
            SELECT
                ?,
                zone_name,
                net_house_code,
                ?,
                ?,
                NULL,
                '',
                '',
                ?,
                ?
            FROM field_net_houses
            WHERE zone_name = ?
            """,
            (work_date, task_key, task_label, str(account_name or "")[:80], timestamp, zone_name),
        )
    conn.commit()
    return conn.total_changes - before_changes


