def normalize_daily_vegetable_manual_entry(
    harvest_date: str,
    crop_name: str,
    net_house_code: str,
    quantity: object,
) -> tuple[str, str, str, float]:
    normalized_harvest_date = str(harvest_date or "").strip()
    normalized_crop_name = str(crop_name or "").strip()
    normalized_net_house_code = str(net_house_code or "").strip()
    if not re.fullmatch(r"\d{4}-\d{2}-\d{2}", normalized_harvest_date):
        raise ValueError("採收日期格式錯誤")
    if not normalized_crop_name:
        raise ValueError("請填寫作物")
    if not normalized_net_house_code:
        raise ValueError("請填寫網室編號或來源")
    quantity_text = str(quantity if quantity is not None else "").strip()
    if not quantity_text:
        raise ValueError("請填寫採收量")
    try:
        normalized_quantity = float(quantity_text)
    except (TypeError, ValueError):
        raise ValueError("採收量必須是數字") from None
    if normalized_quantity < 0:
        raise ValueError("採收量不可小於 0")
    return normalized_harvest_date, normalized_crop_name[:80], normalized_net_house_code[:80], normalized_quantity


