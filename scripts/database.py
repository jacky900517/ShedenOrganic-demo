def validate_net_house_status_harvest_dates(
    record_date: str,
    planted_date: str = "",
    harvest_date: str = "",
    crop_items: list[dict[str, Any]] | None = None,
) -> None:
    effective_planted_date = str(planted_date or record_date or "").strip()
    if not effective_planted_date:
        return
    harvest_dates = [str(harvest_date or "").strip()]
    if isinstance(crop_items, list):
        harvest_dates.extend(
            str(item.get("harvestDate") or "").strip()
            for item in crop_items
            if isinstance(item, dict)
        )
    if any(item_harvest_date and item_harvest_date < effective_planted_date for item_harvest_date in harvest_dates):
        raise ValueError("採收日期不可早於種植日期，可與種植日期同一天")


def net_house_status_planting_end_date(record: dict[str, Any]) -> str:
    crop_items = record.get("cropItems")
    if isinstance(crop_items, list) and crop_items:
        harvest_dates = [
            str(item.get("harvestDate") or "").strip()
            for item in crop_items
            if isinstance(item, dict) and str(item.get("harvestDate") or "").strip()
        ]
        if len(harvest_dates) < len(crop_items):
            return ""
        return max(harvest_dates) if harvest_dates else ""
    return str(record.get("harvestDate") or "").strip()


def is_net_house_status_active_on_date(record: dict[str, Any], selected_date: str) -> bool:
    if record.get("status") != "種植":
        return True
    planted_date = str(record.get("plantedDate") or record.get("recordDate") or "").strip()
    if planted_date and selected_date < planted_date:
        return False
    harvest_date = net_house_status_planting_end_date(record)
    if harvest_date and selected_date > harvest_date:
        return False
    return True


