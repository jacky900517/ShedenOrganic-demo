def save_net_house_status_record(
    conn: sqlite3.Connection,
    record_date: str,
    zone_name: str,
    net_house_code: str,
    status: str,
    crop_name: str = "",
    planted_date: str = "",
    harvest_date: str = "",
    estimated_quantity: float | None = None,
    harvest_quantity: float | None = None,
    crop_items: list[dict[str, Any]] | None = None,
    lunch_marked: bool = False,
) -> dict[str, Any] | None:
    timestamp = taipei_now_text()
    existing_record = get_net_house_status_record(conn, record_date, zone_name, net_house_code)
    normalized_crop_items = normalize_net_house_status_crop_items(
        crop_items,
        crop_name,
        harvest_date,
        estimated_quantity,
        harvest_quantity,
    )
    if not normalized_crop_items:
        restore_record = existing_record or latest_active_planting_status_before_date(
            conn,
            record_date,
            zone_name,
            net_house_code,
        )
        if restore_record:
            restored_items = restore_record.get("cropItems")
            if isinstance(restored_items, list):
                normalized_crop_items = [
                    {
                        "cropName": str(item.get("cropName") or "").strip(),
                        "harvestDate": str(item.get("harvestDate") or "").strip(),
                        "estimatedQuantity": 0 if bool(item.get("destroyed")) else item.get("estimatedQuantity"),
                        "harvestQuantity": 0 if bool(item.get("destroyed")) else item.get("harvestQuantity"),
                        "destroyed": bool(item.get("destroyed")),
                    }
                    for item in restored_items
                    if isinstance(item, dict) and str(item.get("cropName") or "").strip()
                ]
            planted_date = planted_date or str(restore_record.get("plantedDate") or restore_record.get("recordDate") or "")
    if status == "種植":
        validate_net_house_status_harvest_dates(record_date, planted_date, harvest_date, normalized_crop_items)
    first_crop = normalized_crop_items[0] if normalized_crop_items else {}
    conn.execute(
        """
        INSERT INTO net_house_status_records (
            record_date,
            zone_name,
            net_house_code,
            status,
            crop_name,
            planted_date,
            harvest_date,
            estimated_quantity,
            harvest_quantity,
            lunch_marked,
            updated_at
        )
        VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
        ON CONFLICT(record_date, zone_name, net_house_code)
        DO UPDATE SET
            status = excluded.status,
            crop_name = excluded.crop_name,
            planted_date = excluded.planted_date,
            harvest_date = excluded.harvest_date,
            estimated_quantity = excluded.estimated_quantity,
            harvest_quantity = excluded.harvest_quantity,
            lunch_marked = excluded.lunch_marked,
            updated_at = excluded.updated_at
        """,
        (
            record_date,
            zone_name,
            net_house_code,
            status,
            str(first_crop.get("cropName") or ""),
            planted_date,
            str(first_crop.get("harvestDate") or ""),
            first_crop.get("estimatedQuantity"),
            first_crop.get("harvestQuantity"),
            1 if lunch_marked else 0,
            timestamp,
        ),
    )
    conn.execute(
        """
        DELETE FROM net_house_status_record_crops
        WHERE record_date = ? AND zone_name = ? AND net_house_code = ?
        """,
        (record_date, zone_name, net_house_code),
    )
    conn.executemany(
        """
        INSERT INTO net_house_status_record_crops (
            record_date,
            zone_name,
            net_house_code,
            crop_index,
            crop_name,
            harvest_date,
            estimated_quantity,
            harvest_quantity,
            destroyed,
            updated_at
        )
        VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
        """,
        [
            (
                record_date,
                zone_name,
                net_house_code,
                index,
                item["cropName"],
                str(item.get("harvestDate") or ""),
                0 if bool(item.get("destroyed")) else item.get("estimatedQuantity"),
                0 if bool(item.get("destroyed")) else item.get("harvestQuantity"),
                1 if bool(item.get("destroyed")) else 0,
                timestamp,
            )
            for index, item in enumerate(normalized_crop_items, start=1)
        ],
    )
    conn.commit()
    row = conn.execute(
        """
        SELECT
            record_date,
            zone_name,
            net_house_code,
            status,
            crop_name,
            planted_date,
            harvest_date,
            estimated_quantity,
            harvest_quantity,
            lunch_marked
        FROM net_house_status_records
        WHERE record_date = ? AND zone_name = ? AND net_house_code = ?
        """,
        (record_date, zone_name, net_house_code),
    ).fetchone()
    return net_house_status_record_payload(conn, row) if row else None


def crop_items_from_field_work_tasks(tasks: list[dict[str, Any]]) -> list[dict[str, Any]]:
    crop_items: list[dict[str, Any]] = []
    for task in tasks:
        if task.get("taskKey") not in {"directSow", "seedlingTransplant"}:
            continue
        raw_items = task.get("cropItems")
        if isinstance(raw_items, list):
            for item in raw_items:
                if not isinstance(item, dict):
                    continue
                crop_name = str(item.get("cropName") or "").strip()
                if crop_name and not any(existing["cropName"] == crop_name for existing in crop_items):
                    crop_items.append({
                        "cropName": crop_name,
                        "harvestDate": "",
                        "estimatedQuantity": None,
                        "harvestQuantity": None,
                        "destroyed": False,
                    })
        crop_name = str(task.get("cropName") or "").strip()
        if crop_name and not any(existing["cropName"] == crop_name for existing in crop_items):
            crop_items.append({
                "cropName": crop_name,
                "harvestDate": "",
                "estimatedQuantity": None,
                "harvestQuantity": None,
                "destroyed": False,
            })
    return crop_items


def sync_net_house_status_from_field_work(
    conn: sqlite3.Connection,
    work_date: str,
    zone_name: str,
    net_house_code: str,
    tasks: list[dict[str, Any]],
) -> dict[str, Any] | None:
    crop_items = crop_items_from_field_work_tasks(tasks)
    if not crop_items:
        conn.execute(
            """
            DELETE FROM net_house_status_record_crops
            WHERE record_date = ? AND zone_name = ? AND net_house_code = ?
            """,
            (work_date, zone_name, net_house_code),
        )
        conn.execute(
            """
            DELETE FROM net_house_status_records
            WHERE record_date = ?
                AND zone_name = ?
                AND net_house_code = ?
                AND status = '種植'
                AND planted_date = ?
            """,
            (work_date, zone_name, net_house_code, work_date),
        )
        conn.commit()
        return None

    timestamp = taipei_now_text()
    existing_crop_rows = conn.execute(
        """
        SELECT crop_name, harvest_date, estimated_quantity, harvest_quantity, destroyed
        FROM net_house_status_record_crops
        WHERE record_date = ? AND zone_name = ? AND net_house_code = ?
        ORDER BY crop_index
        """,
        (work_date, zone_name, net_house_code),
    ).fetchall()
    existing_by_crop_name = {
        row["crop_name"]: {
            "harvestDate": row["harvest_date"],
            "estimatedQuantity": 0 if bool(row["destroyed"]) else row["estimated_quantity"],
            "harvestQuantity": 0 if bool(row["destroyed"]) else row["harvest_quantity"],
            "destroyed": bool(row["destroyed"]),
        }
        for row in existing_crop_rows
    }
    legacy_row = conn.execute(
        """
        SELECT harvest_date, estimated_quantity, harvest_quantity
        FROM net_house_status_records
        WHERE record_date = ? AND zone_name = ? AND net_house_code = ?
        """,
        (work_date, zone_name, net_house_code),
    ).fetchone()
    for index, item in enumerate(crop_items):
        existing = existing_by_crop_name.get(item["cropName"])
        if existing:
            item["harvestDate"] = existing["harvestDate"]
            item["estimatedQuantity"] = existing["estimatedQuantity"]
            item["harvestQuantity"] = existing["harvestQuantity"]
            item["destroyed"] = existing["destroyed"]
        elif index == 0 and legacy_row:
            item["harvestDate"] = legacy_row["harvest_date"]
            item["estimatedQuantity"] = legacy_row["estimated_quantity"]
            item["harvestQuantity"] = legacy_row["harvest_quantity"]
            item["destroyed"] = False

    conn.execute(
        """
        INSERT INTO net_house_status_records (
            record_date,
            zone_name,
            net_house_code,
            status,
            crop_name,
            planted_date,
            harvest_date,
            estimated_quantity,
            harvest_quantity,
            updated_at
        )
        VALUES (?, ?, ?, '種植', ?, ?, '', NULL, NULL, ?)
        ON CONFLICT(record_date, zone_name, net_house_code)
        DO UPDATE SET
            status = '種植',
            crop_name = excluded.crop_name,
            planted_date = excluded.planted_date,
            updated_at = excluded.updated_at
        """,
        (work_date, zone_name, net_house_code, crop_items[0]["cropName"], work_date, timestamp),
    )
    conn.execute(
        """
        DELETE FROM net_house_status_record_crops
        WHERE record_date = ? AND zone_name = ? AND net_house_code = ?
        """,
        (work_date, zone_name, net_house_code),
    )
    conn.executemany(
        """
        INSERT INTO net_house_status_record_crops (
            record_date,
            zone_name,
            net_house_code,
            crop_index,
            crop_name,
            harvest_date,
            estimated_quantity,
            harvest_quantity,
            destroyed,
            updated_at
        )
        VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
        """,
        [
            (
                work_date,
                zone_name,
                net_house_code,
                index,
                item["cropName"],
                str(item.get("harvestDate") or ""),
                0 if bool(item.get("destroyed")) else item.get("estimatedQuantity"),
                0 if bool(item.get("destroyed")) else item.get("harvestQuantity"),
                1 if bool(item.get("destroyed")) else 0,
                timestamp,
            )
            for index, item in enumerate(crop_items, start=1)
        ],
    )
    conn.commit()
    row = conn.execute(
        """
        SELECT
            record_date,
            zone_name,
            net_house_code,
            status,
            crop_name,
            planted_date,
            harvest_date,
            estimated_quantity,
            harvest_quantity,
            lunch_marked
        FROM net_house_status_records
        WHERE record_date = ? AND zone_name = ? AND net_house_code = ?
        """,
        (work_date, zone_name, net_house_code),
    ).fetchone()
    if not row:
        return None
    return net_house_status_record_payload(conn, row)


