    conn.execute("DELETE FROM field_zones")
    for zone_index, zone in enumerate(zones):
        zone_name = str(zone.get("name") or "").strip()
        if not zone_name:
            continue
        sort_order = int(zone.get("sortOrder") if zone.get("sortOrder") is not None else zone_index)
