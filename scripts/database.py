def taipei_now() -> datetime:
    return datetime.now(TAIPEI_TZ)


def taipei_now_text() -> str:
    return taipei_now().isoformat(timespec="seconds")


def database_backend() -> str:
    backend = DATABASE_BACKEND.strip().lower()
    if backend not in {"sqlite", "postgres", "postgresql"}:
        raise ValueError("DATABASE_BACKEND must be sqlite or postgres")
    return "postgres" if backend == "postgresql" else backend


def is_postgres_backend() -> bool:
    return database_backend() == "postgres"


def split_sql_script(script: str) -> list[str]:
    statements = []
    current = []
    in_single = False
    in_double = False
    index = 0
    while index < len(script):
        char = script[index]
        current.append(char)
        if char == "'" and not in_double:
            in_single = not in_single
        elif char == '"' and not in_single:
            in_double = not in_double
        elif char == ";" and not in_single and not in_double:
            statement = "".join(current).strip().rstrip(";").strip()
            if statement:
                statements.append(statement)
            current = []
        index += 1
    tail = "".join(current).strip()
    if tail:
        statements.append(tail)
    return statements


def translate_postgres_sql(sql: str) -> str:
    translated = sql
    stripped = translated.lstrip()
    if stripped.upper().startswith("INSERT OR IGNORE INTO "):
        leading = translated[: len(translated) - len(stripped)]
        translated = leading + "INSERT INTO " + stripped[len("INSERT OR IGNORE INTO "):]
        if "ON CONFLICT" not in translated.upper():
            translated = translated.rstrip().rstrip(";") + " ON CONFLICT DO NOTHING"
    translated = translated.replace("?", "%s")
    return translated


def maybe_add_returning_id(sql: str) -> tuple[str, bool]:
    normalized = re.sub(r"\s+", " ", sql.strip()).upper()
    if " RETURNING " in normalized:
        return sql, False
    insert_tables = (
        "INSERT INTO HARVEST_MESSAGES ",
        "INSERT INTO FIELD_WORK_MESSAGES ",
        "INSERT INTO DAILY_VEGETABLE_MANUAL_ENTRIES ",
        "INSERT INTO USERS ",
    )
    if any(normalized.startswith(prefix) for prefix in insert_tables):
        return sql.rstrip().rstrip(";") + " RETURNING id", True
    return sql, False


class PostgresCursor:
    def __init__(self, cursor: Any, lastrowid: int | None = None):
        self._cursor = cursor
        self.lastrowid = lastrowid

    @property
    def rowcount(self) -> int:
        return int(getattr(self._cursor, "rowcount", -1))

    def fetchone(self) -> Any:
        return self._cursor.fetchone()

    def fetchall(self) -> list[Any]:
        return self._cursor.fetchall()


class PostgresConnection:
    def __init__(self) -> None:
        if not DATABASE_URL:
            raise ValueError("DATABASE_URL is required when DATABASE_BACKEND=postgres")
        try:
            import psycopg
            from psycopg.rows import dict_row
        except ImportError as exc:
            raise RuntimeError("PostgreSQL support requires the psycopg package") from exc
        self._conn = psycopg.connect(DATABASE_URL, row_factory=dict_row)
        self._total_changes = 0

    @property
    def total_changes(self) -> int:
        return self._total_changes

    def execute(self, sql: str, params: Iterable[Any] | None = None) -> PostgresCursor:
        translated = translate_postgres_sql(sql)
        translated, fetch_lastrowid = maybe_add_returning_id(translated)
        cursor = self._conn.execute(translated, tuple(params or ()))
        lastrowid = None
        if fetch_lastrowid:
            row = cursor.fetchone()
            lastrowid = int(row["id"]) if row and row.get("id") is not None else None
        if cursor.rowcount and cursor.rowcount > 0:
            self._total_changes += cursor.rowcount
        return PostgresCursor(cursor, lastrowid)

    def executemany(self, sql: str, params: Iterable[Iterable[Any]]) -> PostgresCursor:
        translated = translate_postgres_sql(sql)
        cursor = self._conn.cursor()
        cursor.executemany(translated, [tuple(item) for item in params])
        if cursor.rowcount and cursor.rowcount > 0:
            self._total_changes += cursor.rowcount
        return PostgresCursor(cursor)

    def executescript(self, script: str) -> None:
        for statement in split_sql_script(script):
            self.execute(statement)

    def commit(self) -> None:
        self._conn.commit()

    def rollback(self) -> None:
        self._conn.rollback()

    def close(self) -> None:
        self._conn.close()


def connect() -> Any:
    if is_postgres_backend():
        return PostgresConnection()
    DB_PATH.parent.mkdir(parents=True, exist_ok=True)
    conn = sqlite3.connect(DB_PATH)
    conn.row_factory = sqlite3.Row
    conn.execute("PRAGMA journal_mode=WAL")
    return conn


def init_postgres_db(conn: Any) -> None:
    conn.execute("SELECT pg_advisory_xact_lock(?)", (POSTGRES_SCHEMA_LOCK_ID,))
    conn.executescript(POSTGRES_SCHEMA)
    conn.execute(
        """
        UPDATE users
        SET display_name = CASE
            WHEN username = 'root' THEN 'admin'
            ELSE username
        END
        WHERE COALESCE(display_name, '') = ''
        """
    )
    conn.commit()


