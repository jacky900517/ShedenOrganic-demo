def get_field_work_message_photos(
    conn: sqlite3.Connection,
    message_ids: list[int],
) -> dict[int, list[dict[str, Any]]]:
    photos_by_message: dict[int, list[dict[str, Any]]] = {message_id: [] for message_id in message_ids}
    if not message_ids:
        return photos_by_message
    for index in range(0, len(message_ids), 900):
        chunk = message_ids[index:index + 900]
        placeholders = ",".join("?" for _ in chunk)
        rows = conn.execute(
            f"""
            SELECT id, message_id, file_name, original_name, content_type, url_path, created_at
            FROM field_work_message_photos
            WHERE message_id IN ({placeholders})
            ORDER BY message_id, id
            """,
            chunk,
        ).fetchall()
        for row in rows:
            photos_by_message.setdefault(row["message_id"], []).append(field_work_message_photo_payload(row))
    return photos_by_message


