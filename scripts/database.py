def active_net_house_status_on_date(
    conn: sqlite3.Connection,
    record_date: str,
    zone_name: str,
    net_house_code: str,
) -> str:
    rows = conn.execute(
        """
        SELECT
            r.record_date,
            r.status,
            r.crop_name,
            r.planted_date,
            r.harvest_date,
            COUNT(c.crop_name) AS crop_count,
            SUM(
                CASE
                    WHEN c.crop_name IS NOT NULL AND NULLIF(c.harvest_date, '') IS NULL THEN 1
                    ELSE 0
                END
            ) AS blank_crop_harvest_count,
            MAX(NULLIF(c.harvest_date, '')) AS crop_harvest_date
        FROM net_house_status_records r
        LEFT JOIN net_house_status_record_crops c
            ON c.record_date = r.record_date
            AND c.zone_name = r.zone_name
            AND c.net_house_code = r.net_house_code
        WHERE r.zone_name = ?
            AND r.net_house_code = ?
            AND r.record_date <= ?
        GROUP BY r.record_date, r.status, r.crop_name, r.planted_date, r.harvest_date
        ORDER BY r.record_date DESC
        """,
        (zone_name, net_house_code, record_date),
    ).fetchall()
    for row in rows:
        status = str(row["status"] or "")
        if status != "種植":
            return status
        planted_date = str(row["planted_date"] or row["record_date"] or "")
        if planted_date and record_date < planted_date:
            continue
        crop_count = int(row["crop_count"] or 0)
        if crop_count == 0 and not str(row["crop_name"] or "").strip():
            continue
        blank_crop_harvest_count = int(row["blank_crop_harvest_count"] or 0)
        harvest_date = "" if crop_count and blank_crop_harvest_count else str(row["crop_harvest_date"] or row["harvest_date"] or "")
        if harvest_date and record_date >= harvest_date:
            continue
        return status
    return ""


