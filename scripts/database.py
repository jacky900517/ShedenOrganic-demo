def get_setting(conn: sqlite3.Connection, setting_key: str) -> Any | None:
    row = conn.execute(
        "SELECT setting_value FROM app_settings WHERE setting_key = ?",
        (setting_key,),
    ).fetchone()
    if not row:
        return None
    return json.loads(row["setting_value"])


